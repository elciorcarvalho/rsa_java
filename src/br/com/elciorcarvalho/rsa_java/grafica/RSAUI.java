    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        /**
         * *
         * Gera as chaves para a cripto grafia RSA.
         */
        RSAKeyPairGenerator keyPairGenerator;
        try {
            keyPairGenerator = new RSAKeyPairGenerator();
            keyPairGenerator.writeToFile("RSA/publicKey", keyPairGenerator.getPublicKey().getEncoded());
            keyPairGenerator.writeToFile("RSA/privateKey", keyPairGenerator.getPrivateKey().getEncoded());

            // Chave Publica
            jTextArea1.setLineWrap(true);
            jTextArea1.setWrapStyleWord(true);
            jTextArea1.setText(Base64.getEncoder().encodeToString(keyPairGenerator.getPublicKey().getEncoded()));

            // Chave Privada
            jTextArea2.setLineWrap(true);
            jTextArea2.setWrapStyleWord(true);
            jTextArea2.setText(Base64.getEncoder().encodeToString(keyPairGenerator.getPrivateKey().getEncoded()));

        } catch (NoSuchAlgorithmException | IOException ex) {
            Logger.getLogger(RSAUI.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        /**
         * *
         * Criptografa uma sentenca.
         */
        String publicKey = jTextArea1.getText();
        String frase = jTextArea3.getText();

        if (publicKey == null || publicKey.equals("")) {
            jTextArea4.setText("É necessário fornecer uma chave pública!");
        } else {
            String encryptedString;
            try {
                encryptedString = Base64.getEncoder().encodeToString(encrypt(frase, publicKey));
                jTextArea4.setText(encryptedString);
            } catch (BadPaddingException | IllegalBlockSizeException | InvalidKeyException | NoSuchPaddingException | NoSuchAlgorithmException ex) {
                Logger.getLogger(RSAUI.class.getName()).log(Level.SEVERE, null, ex);
            }
        }


    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        /**
         * *
         * Descriptografa uma cifra.
         */
        String privateKey = jTextArea2.getText();
        String encryptedString = jTextArea4.getText();

        if (privateKey == null || privateKey.equals("")) {
            jTextArea3.setText("É necessário fornecer uma chave privada!");
        } else {
            String decryptedString;
            try {
                decryptedString = RSAUtil.decrypt(encryptedString, privateKey);
                jTextArea3.setText(decryptedString);
            } catch (IllegalBlockSizeException | InvalidKeyException | BadPaddingException | NoSuchAlgorithmException | NoSuchPaddingException ex) {
                Logger.getLogger(RSAUI.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_jButton3ActionPerformed

